# ACE evaluation with forces for --trim=safe compilation
# This test demonstrates the complete runtime path including force computation.
#
# Workflow for LAMMPS export:
# 1. Build ACE model in full Julia (construction code)
# 2. Export tensor structures to this file as constants
# 3. Compile with --trim=safe
# 4. Binary receives atomic positions, returns energies/forces

using StaticArrays
using LinearAlgebra: norm, dot
using SparseArrays: sparse, SparseMatrixCSC
using Polynomials4ML
const P4ML = Polynomials4ML
using EquivariantTensors
const ET = EquivariantTensors

# ============================================================================
# HARDCODED MODEL STRUCTURES
# In production, these would be generated by a model export script
# ============================================================================

# Simple 2-body + 3-body ACE model for single element (Si-like)
# Pre-computed from: maxl=2, order=2, totaldegree=6

# PooledSparseProduct spec: (Rnl_idx, Ylm_idx) pairs
const ABASIS = ET.PooledSparseProduct([
    (1, 1), (2, 1), (3, 1), (4, 1), (5, 1),  # l=0 terms
    (2, 2), (2, 3), (2, 4),                   # l=1 terms
    (3, 2), (3, 3), (3, 4),
    (4, 5), (4, 6), (4, 7), (4, 8), (4, 9),  # l=2 terms
])

# SparseSymmProd for body order 1 (just A basis indices)
const AABASIS = ET.SparseSymmProd([
    [1], [2], [3], [4], [5],      # 1-body terms (single A basis)
    [6], [7], [8], [9], [10],     # more 1-body
])

# Coupling matrix (identity for this simple case)
const A2BMAP = (sparse(1:10, 1:10, ones(Float64, 10), 10, 10),)

# Readable specification for debugging (stored in meta dict for original API)
const AA_SPEC = [
    [(n=1, l=0, m=0)], [(n=2, l=0, m=0)], [(n=3, l=0, m=0)],
    [(n=4, l=0, m=0)], [(n=5, l=0, m=0)], [(n=1, l=1, m=-1)],
    [(n=1, l=1, m=0)], [(n=1, l=1, m=1)], [(n=2, l=1, m=-1)],
    [(n=2, l=1, m=0)],
]::Vector{Vector{@NamedTuple{n::Int, l::Int, m::Int}}}

# Assemble the full SparseACEbasis using original Dict-based constructor
const META = Dict{String, Any}("𝔸spec" => AA_SPEC)
const TENSOR = ET.SparseACEbasis(ABASIS, AABASIS, A2BMAP, META)

# Radial basis specification
const R_SPEC = [(n=1, l=0), (n=2, l=0), (n=1, l=1), (n=2, l=1), (n=1, l=2)]
const MAXL = 2
const YBASIS = P4ML.real_sphericalharmonics(MAXL)
const RCUT = 5.0  # Angstrom

# Model weights (normally fitted, here random for testing)
const WEIGHTS = Float64[0.1, -0.2, 0.15, -0.1, 0.05, 0.08, -0.12, 0.1, -0.05, 0.03]

# ============================================================================
# EVALUATION FUNCTIONS - These must be --trim compatible
# ============================================================================

# Smooth cutoff function
@inline function cutoff_env(r::Float64, rcut::Float64)
    if r >= rcut
        return 0.0
    end
    x = r / rcut
    return (1 - x)^2
end

@inline function cutoff_env_d(r::Float64, rcut::Float64)
    if r >= rcut
        return 0.0
    end
    x = r / rcut
    return -2 * (1 - x) / rcut
end

# Radial basis evaluation
function eval_radial(r::Float64, r_spec, rcut::Float64)
    env = cutoff_env(r, rcut)
    Rnl = zeros(Float64, length(r_spec))
    for (i, spec) in enumerate(r_spec)
        n, l = spec.n, spec.l
        # Simple polynomial radial basis
        Rnl[i] = env * exp(-r) * r^l / (n + l)
    end
    return Rnl
end

# Radial basis with derivative
function eval_radial_d(r::Float64, r_spec, rcut::Float64)
    env = cutoff_env(r, rcut)
    env_d = cutoff_env_d(r, rcut)

    Rnl = zeros(Float64, length(r_spec))
    dRnl = zeros(Float64, length(r_spec))

    for (i, spec) in enumerate(r_spec)
        n, l = spec.n, spec.l
        f = exp(-r) * r^l / (n + l)
        # df/dr = exp(-r) * (l*r^(l-1) - r^l) / (n+l) = f * (l/r - 1)
        df = l > 0 ? f * (l/r - 1.0) : -f

        Rnl[i] = env * f
        dRnl[i] = env_d * f + env * df
    end
    return Rnl, dRnl
end

# Compute Rnl and Ylm matrices for all neighbors
function compute_embeddings(Rs::Vector{SVector{3, Float64}}, r_spec, ybasis, rcut::Float64)
    nneigh = length(Rs)
    nbasis_r = length(r_spec)
    nbasis_y = length(ybasis)

    Rnl = zeros(Float64, nneigh, nbasis_r)
    Ylm = zeros(Float64, nneigh, nbasis_y)

    for j in 1:nneigh
        r = norm(Rs[j])
        if r < rcut && r > 1e-10
            Rnl[j, :] = eval_radial(r, r_spec, rcut)
            rhat = Rs[j] / r
            P4ML.evaluate!(view(Ylm, j, :), ybasis, rhat)
        end
    end

    return Rnl, Ylm
end

# Site energy evaluation
function site_energy(Rs::Vector{SVector{3, Float64}}, tensor, r_spec, ybasis,
                     rcut::Float64, weights::Vector{Float64})
    Rnl, Ylm = compute_embeddings(Rs, r_spec, ybasis, rcut)
    BB = ET.evaluate(tensor, Rnl, Ylm, NamedTuple(), NamedTuple())
    B = BB[1]  # L=0 channel
    return dot(weights, B)
end

# Site energy with forces using finite differences (for validation)
function site_energy_forces_fd(Rs::Vector{SVector{3, Float64}}, tensor, r_spec, ybasis,
                               rcut::Float64, weights::Vector{Float64}; h::Float64=1e-5)
    E0 = site_energy(Rs, tensor, r_spec, ybasis, rcut, weights)

    forces = Vector{SVector{3, Float64}}(undef, length(Rs))
    for j in 1:length(Rs)
        f = zeros(3)
        for α in 1:3
            Rs_p = copy(Rs)
            Rs_m = copy(Rs)
            e_α = zeros(3); e_α[α] = h
            Rs_p[j] = Rs[j] + SVector{3}(e_α)
            Rs_m[j] = Rs[j] - SVector{3}(e_α)
            Ep = site_energy(Rs_p, tensor, r_spec, ybasis, rcut, weights)
            Em = site_energy(Rs_m, tensor, r_spec, ybasis, rcut, weights)
            f[α] = -(Ep - Em) / (2h)
        end
        forces[j] = SVector{3}(f)
    end

    return E0, forces
end

# ============================================================================
# MAIN - Interface for LAMMPS-style calls
# ============================================================================

function (@main)(ARGS)
    # Test configuration: 4 neighbors in tetrahedral arrangement
    Rs = [
        SVector(2.35, 0.0, 0.0),
        SVector(-0.78, 2.22, 0.0),
        SVector(-0.78, -1.11, 1.92),
        SVector(-0.78, -1.11, -1.92)
    ]

    println(Core.stdout, "=== ACE Evaluation with Forces Test ===")
    println(Core.stdout, "Tensor length: ", length(TENSOR))
    println(Core.stdout, "Number of basis functions: ", length(WEIGHTS))
    println(Core.stdout, "Number of neighbors: ", length(Rs))
    println(Core.stdout, "Cutoff: ", RCUT, " Angstrom")
    println(Core.stdout, "")

    # Compute energy
    E = site_energy(Rs, TENSOR, R_SPEC, YBASIS, RCUT, WEIGHTS)
    println(Core.stdout, "Site energy: ", E, " eV")

    # Compute forces via finite differences
    E_fd, F_fd = site_energy_forces_fd(Rs, TENSOR, R_SPEC, YBASIS, RCUT, WEIGHTS)
    println(Core.stdout, "")
    println(Core.stdout, "Forces (finite difference):")
    for (j, f) in enumerate(F_fd)
        # Manual formatting to avoid SVector show methods (not trim-safe)
        println(Core.stdout, "  F[", j, "] = [", f[1], ", ", f[2], ", ", f[3], "]")
    end

    println(Core.stdout, "")
    println(Core.stdout, "Evaluation successful!")

    return 0
end
